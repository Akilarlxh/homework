/*编程计算1+1/3!+1/5!+...+1/(2n-1)!,直到最
后一项小于0.000001，并统计一共计算了多少项*/
#include <stdio.h>
int main()
{
	float a=1,b=1,cnt,n=3;
	//a用来计算每一项的值，b用来存放所有数的和 
	for(cnt=0;;cnt++){
	//cnt用来计数，n=3是因为没有计算1的阶乘 
		a=a*1/((n-1)*n);
		n+=2;
		b+=a;
		if(a<0.000001)
		//如果最后一项小于0.000001，就结束循环
		break;
	} 
	printf("%f\n%.0f",b,cnt+2);
	//第一项1不是运算出的，而是直接给的 
}
 
 
